
from turtle import *

# change line width
pensize(5)

# change to an actual turtle
shape("turtle")

# Go to first position
penup()
left(180)
forward(50)
right(90)
forward(50)

# Draw top line
pendown()
right(90)
forward(100)

# Draw right line
right(90)
forward(100)

# Draw bottom line 
right(90)
forward(100)

# Draw left line
right(90)
forward(100)

done()

